-- Registro de dispensación de medicamentos a pacientes
-- Máquina de estados: PENDIENTE → DISPENSADA | CANCELADA
-- ============================================================

CREATE TABLE FAR_DISPENSACIONES (
    DIS_ID              NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY
                                        CONSTRAINT PK_FAR_DIS PRIMARY KEY,
    HOSPITAL_ID         NUMBER(10)      NOT NULL,

    -- Relaciones principales
    MED_ID              NUMBER          NOT NULL,
    PAC_ID              NUMBER          NOT NULL,
    MEDICO_PRES_ID      NUMBER,
    DISPENSADO_POR_ID   NUMBER,

    -- Origen opcional (trazabilidad entre módulos)
    EMG_ID              NUMBER,
    ENC_ID              NUMBER,
    CIT_ID              NUMBER,

    -- Prescripción
    CANTIDAD            NUMBER(10,3)    NOT NULL,
    DOSIS               VARCHAR2(200),
    FRECUENCIA          VARCHAR2(100),
    DURACION_DIAS       NUMBER(3),
    VIA_ADMIN           VARCHAR2(30)    DEFAULT 'ORAL'
                        CONSTRAINT CK_FAR_DIS_VIA CHECK (
                            VIA_ADMIN IN ('ORAL','INTRAVENOSA','INTRAMUSCULAR','SUBCUTANEA',
                                          'TOPICA','INHALATORIA','SUBLINGUAL','RECTAL',
                                          'NASAL','OFTALMICA','OTICA','OTRO')
                        ),
    INDICACIONES        VARCHAR2(500),

    -- Estado (máquina de estados)
    ESTADO              VARCHAR2(20)    DEFAULT 'PENDIENTE'
                        CONSTRAINT CK_FAR_DIS_ESTADO CHECK (
                            ESTADO IN ('PENDIENTE','DISPENSADA','CANCELADA')
                        ),

    -- Fechas
    FECHA_PRESCRIPCION  DATE            NOT NULL,
    FECHA_DISPENSACION  DATE,
    HORA_DISPENSACION   TIMESTAMP,

    -- Notas
    NOTAS_FARMACIA      VARCHAR2(1000),
    MOTIVO_CANCELACION  VARCHAR2(500),

    -- Auditoría HIPAA
    ACTIVO              NUMBER(1)       DEFAULT 1
                        CONSTRAINT CK_FAR_DIS_ACTIVO CHECK (ACTIVO IN (0,1)),
    CREATED_BY          NUMBER,
    CREATED_AT          TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY          NUMBER,
    UPDATED_AT          TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,

    -- Foreign Keys
    CONSTRAINT FK_FAR_DIS_MED       FOREIGN KEY (MED_ID)            REFERENCES FAR_MEDICAMENTOS(MED_ID),
    CONSTRAINT FK_FAR_DIS_PAC       FOREIGN KEY (PAC_ID)            REFERENCES PAC_PACIENTES(PAC_ID),
    CONSTRAINT FK_FAR_DIS_MEDICO    FOREIGN KEY (MEDICO_PRES_ID)    REFERENCES SEC_USUARIOS(USU_ID),
    CONSTRAINT FK_FAR_DIS_DISPENSO  FOREIGN KEY (DISPENSADO_POR_ID) REFERENCES SEC_USUARIOS(USU_ID),
    CONSTRAINT FK_FAR_DIS_EMG       FOREIGN KEY (EMG_ID)            REFERENCES EMG_EMERGENCIAS(EMG_ID),
    CONSTRAINT FK_FAR_DIS_ENC       FOREIGN KEY (ENC_ID)            REFERENCES ENC_ENCAMAMIENTOS(ENC_ID),
    CONSTRAINT FK_FAR_DIS_CIT       FOREIGN KEY (CIT_ID)            REFERENCES CIT_CITAS(CIT_ID),
    CONSTRAINT FK_FAR_DIS_CREATEDBY FOREIGN KEY (CREATED_BY)        REFERENCES SEC_USUARIOS(USU_ID),
    CONSTRAINT FK_FAR_DIS_UPDATEDBY FOREIGN KEY (UPDATED_BY)        REFERENCES SEC_USUARIOS(USU_ID)
) TABLESPACE PHI_DATA;

-- Índices
CREATE INDEX IDX_FAR_DIS_HOSPITAL  ON FAR_DISPENSACIONES (HOSPITAL_ID)                          TABLESPACE PHI_IDX;
CREATE INDEX IDX_FAR_DIS_PACIENTE  ON FAR_DISPENSACIONES (HOSPITAL_ID, PAC_ID)                  TABLESPACE PHI_IDX;
CREATE INDEX IDX_FAR_DIS_MED       ON FAR_DISPENSACIONES (HOSPITAL_ID, MED_ID)                  TABLESPACE PHI_IDX;
CREATE INDEX IDX_FAR_DIS_ESTADO    ON FAR_DISPENSACIONES (HOSPITAL_ID, ESTADO, FECHA_PRESCRIPCION) TABLESPACE PHI_IDX;
CREATE INDEX IDX_FAR_DIS_FECHA     ON FAR_DISPENSACIONES (HOSPITAL_ID, FECHA_PRESCRIPCION)      TABLESPACE PHI_IDX;

-- Trigger auditoría UPDATED_AT
CREATE OR REPLACE TRIGGER TRG_FAR_DIS_UPD
    BEFORE UPDATE ON FAR_DISPENSACIONES
    FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSTIMESTAMP;
END;
/

COMMIT;
