CREATE TABLE ENC_CAMAS (
    CAMA_ID         NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY
                                    CONSTRAINT PK_ENC_CAMAS PRIMARY KEY,
    HOSPITAL_ID     NUMBER          NOT NULL,

    -- Ubicación
    NUMERO_CAMA     VARCHAR2(10)    NOT NULL,       -- Ej: '101-A', 'UCI-3'
    PISO            VARCHAR2(10),                   -- Ej: '1', '2', 'PB', 'UCI'
    SALA            VARCHAR2(50),                   -- Ej: 'Medicina Interna', 'UCI'
    TIPO_CAMA       VARCHAR2(30)    NOT NULL
                        CONSTRAINT CHK_ENC_TIPO_CAMA CHECK (
                            TIPO_CAMA IN ('GENERAL','UCI','PEDIATRICA','MATERNIDAD',
                                          'CIRUGIA','AISLAMIENTO','OBSERVACION')
                        ),

    -- Estado operativo
    ESTADO          VARCHAR2(20)    NOT NULL    DEFAULT 'DISPONIBLE'
                        CONSTRAINT CHK_ENC_CAMA_ESTADO CHECK (
                            ESTADO IN ('DISPONIBLE','OCUPADA','RESERVADA','MANTENIMIENTO')
                        ),

    -- Equipamiento
    TIENE_OXIGENO   NUMBER(1)       DEFAULT 0   CONSTRAINT CHK_ENC_O2    CHECK (TIENE_OXIGENO   IN (0,1)),
    TIENE_MONITOR   NUMBER(1)       DEFAULT 0   CONSTRAINT CHK_ENC_MON   CHECK (TIENE_MONITOR   IN (0,1)),
    TIENE_VENTILADOR NUMBER(1)      DEFAULT 0   CONSTRAINT CHK_ENC_VENT  CHECK (TIENE_VENTILADOR IN (0,1)),

    OBSERVACIONES   VARCHAR2(500),

    -- Auditoría
    ACTIVO          NUMBER(1)       DEFAULT 1
                        CONSTRAINT CHK_ENC_CAMA_ACT CHECK (ACTIVO IN (0,1)),
    CREATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT FK_ENC_CAMA_HOSP FOREIGN KEY (HOSPITAL_ID) REFERENCES SEC_HOSPITALES(HOSP_ID),
    CONSTRAINT UQ_ENC_CAMA      UNIQUE (HOSPITAL_ID, NUMERO_CAMA)
)
TABLESPACE HT_DATA;

CREATE INDEX IDX_ENC_CAMA_HOSP   ON ENC_CAMAS (HOSPITAL_ID)                   TABLESPACE HT_IDX;
CREATE INDEX IDX_ENC_CAMA_ESTADO ON ENC_CAMAS (HOSPITAL_ID, ESTADO, TIPO_CAMA) TABLESPACE HT_IDX;

CREATE OR REPLACE TRIGGER TRG_ENC_CAMAS_UPD
    BEFORE UPDATE ON ENC_CAMAS FOR EACH ROW
BEGIN :NEW.UPDATED_AT := SYSTIMESTAMP; END;
/

COMMENT ON TABLE  ENC_CAMAS            IS 'Catálogo de camas hospitalarias';
COMMENT ON COLUMN ENC_CAMAS.ESTADO     IS 'DISPONIBLE|OCUPADA|RESERVADA|MANTENIMIENTO';
COMMENT ON COLUMN ENC_CAMAS.TIPO_CAMA  IS 'GENERAL|UCI|PEDIATRICA|MATERNIDAD|CIRUGIA|AISLAMIENTO|OBSERVACION';
