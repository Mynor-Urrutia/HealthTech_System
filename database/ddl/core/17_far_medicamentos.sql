-- Catálogo de medicamentos por hospital
-- HIPAA: datos en tablespace PHI_DATA (TDE en PROD)
-- VPD:   HOSPITAL_ID presente para filtrado automático
-- ============================================================

CREATE TABLE FAR_MEDICAMENTOS (
    MED_ID              NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY
                                        CONSTRAINT PK_FAR_MED PRIMARY KEY,
    HOSPITAL_ID         NUMBER(10)      NOT NULL,

    -- Identificación del medicamento
    NOMBRE_GENERICO     VARCHAR2(200)   NOT NULL,
    NOMBRE_COMERCIAL    VARCHAR2(200),
    PRINCIPIO_ACTIVO    VARCHAR2(200),
    CONCENTRACION       VARCHAR2(100),

    -- Clasificación
    FORMA_FARMA         VARCHAR2(30)    DEFAULT 'TABLETA'
                        CONSTRAINT CK_FAR_MED_FORMA CHECK (
                            FORMA_FARMA IN ('TABLETA','CAPSULA','JARABE','INYECTABLE',
                                            'CREMA','SOLUCION','SUPOSITORIO','GOTAS','PARCHE','OTRO')
                        ),
    CATEGORIA           VARCHAR2(50)    DEFAULT 'OTRO'
                        CONSTRAINT CK_FAR_MED_CAT CHECK (
                            CATEGORIA IN ('ANALGESICO','ANTIBIOTICO','ANTIHIPERTENSIVO',
                                          'ANTIDIABETICO','VITAMINA','ANTIINFLAMATORIO',
                                          'CARDIOVASCULAR','NEUROLOGICO','RESPIRATORIO','OTRO')
                        ),
    UNIDAD_MEDIDA       VARCHAR2(30)    DEFAULT 'tableta',

    -- Inventario
    STOCK_ACTUAL        NUMBER(10)      DEFAULT 0 NOT NULL,
    STOCK_MINIMO        NUMBER(10)      DEFAULT 10 NOT NULL,
    PRECIO_UNITARIO     NUMBER(10,2)    DEFAULT 0,

    -- Control
    REQUIERE_RECETA     NUMBER(1)       DEFAULT 0
                        CONSTRAINT CK_FAR_MED_RECETA CHECK (REQUIERE_RECETA IN (0,1)),
    ACTIVO              NUMBER(1)       DEFAULT 1
                        CONSTRAINT CK_FAR_MED_ACTIVO CHECK (ACTIVO IN (0,1)),

    -- Auditoría
    CREATED_BY          NUMBER,
    CREATED_AT          TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_BY          NUMBER,
    UPDATED_AT          TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,

    -- Foreign Keys
    CONSTRAINT FK_FAR_MED_HOSPITAL  FOREIGN KEY (HOSPITAL_ID)  REFERENCES SEC_HOSPITALES(HOSP_ID),
    CONSTRAINT FK_FAR_MED_CREATEDBY FOREIGN KEY (CREATED_BY)   REFERENCES SEC_USUARIOS(USU_ID),
    CONSTRAINT FK_FAR_MED_UPDATEDBY FOREIGN KEY (UPDATED_BY)   REFERENCES SEC_USUARIOS(USU_ID)
) TABLESPACE PHI_DATA;

-- Índices
CREATE INDEX IDX_FAR_MED_HOSPITAL  ON FAR_MEDICAMENTOS (HOSPITAL_ID)                       TABLESPACE PHI_IDX;
CREATE INDEX IDX_FAR_MED_NOMBRE    ON FAR_MEDICAMENTOS (HOSPITAL_ID, NOMBRE_GENERICO)      TABLESPACE PHI_IDX;
CREATE INDEX IDX_FAR_MED_CATEGORIA ON FAR_MEDICAMENTOS (HOSPITAL_ID, CATEGORIA)            TABLESPACE PHI_IDX;
CREATE INDEX IDX_FAR_MED_STOCK     ON FAR_MEDICAMENTOS (HOSPITAL_ID, STOCK_ACTUAL)         TABLESPACE PHI_IDX;

-- Trigger auditoría UPDATED_AT
CREATE OR REPLACE TRIGGER TRG_FAR_MED_UPD
    BEFORE UPDATE ON FAR_MEDICAMENTOS
    FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSTIMESTAMP;
END;
/

COMMIT;
