-- Módulo M02 — Contactos de Emergencia del Paciente
-- Compatible: Oracle 19c RAC + Oracle 21c XE
-- PHI → tablespace PHI_DATA
-- =============================================================

BEGIN
  EXECUTE IMMEDIATE '
    CREATE TABLE PAC_CONTACTOS_EMERGENCIA (
      CONTACTO_ID      NUMBER         GENERATED BY DEFAULT ON NULL AS IDENTITY
                                      CONSTRAINT PK_PAC_CONTACTOS PRIMARY KEY,
      HOSPITAL_ID      NUMBER         NOT NULL,
      PAC_ID           NUMBER         NOT NULL,

      -- Datos del contacto (PHI)
      NOMBRE_COMPLETO  VARCHAR2(200)  NOT NULL,
      PARENTESCO       VARCHAR2(50)   NOT NULL,
      TELEFONO         VARCHAR2(20)   NOT NULL,
      TELEFONO_ALT     VARCHAR2(20)   DEFAULT '''' '''' NOT NULL,
      EMAIL            VARCHAR2(150)  DEFAULT '''' '''' NOT NULL,
      DIRECCION        VARCHAR2(500)  DEFAULT '''' '''' NOT NULL,

      -- Responsable legal
      ES_RESPONSABLE   NUMBER(1)      DEFAULT 0 NOT NULL,

      -- Estado
      ACTIVO           NUMBER(1)      DEFAULT 1 NOT NULL,
      CREATED_AT       TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
      UPDATED_AT       TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,

      -- Restricciones
      CONSTRAINT CHK_CTT_RESP   CHECK (ES_RESPONSABLE IN (0, 1)),
      CONSTRAINT CHK_CTT_ACTIVO CHECK (ACTIVO         IN (0, 1))
    ) TABLESPACE PHI_DATA
  ';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF;
END;
/

-- FK: Paciente
BEGIN
  EXECUTE IMMEDIATE '
    ALTER TABLE PAC_CONTACTOS_EMERGENCIA
    ADD CONSTRAINT FK_CTT_PACIENTE
      FOREIGN KEY (PAC_ID) REFERENCES PAC_PACIENTES(PAC_ID)
  ';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE = -2275 THEN NULL; ELSE RAISE; END IF;
END;
/

-- Índice por paciente
BEGIN EXECUTE IMMEDIATE 'CREATE INDEX IDX_CTT_PACIENTE ON PAC_CONTACTOS_EMERGENCIA(HOSPITAL_ID, PAC_ID)';
EXCEPTION WHEN OTHERS THEN IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF; END;
/

-- Trigger UPDATED_AT
BEGIN
  EXECUTE IMMEDIATE '
    CREATE OR REPLACE TRIGGER TRG_PAC_CONTACTOS_UPD
    BEFORE UPDATE ON PAC_CONTACTOS_EMERGENCIA
    FOR EACH ROW
    BEGIN
      :NEW.UPDATED_AT := SYSTIMESTAMP;
    END;
  ';
EXCEPTION WHEN OTHERS THEN IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF;
END;
/

COMMENT ON TABLE  PAC_CONTACTOS_EMERGENCIA               IS 'Contactos de emergencia del paciente (PHI)';
COMMENT ON COLUMN PAC_CONTACTOS_EMERGENCIA.ES_RESPONSABLE IS '1 = Responsable legal del paciente';
