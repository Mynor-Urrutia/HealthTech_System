-- Compatible: Oracle 19c RAC + Oracle 21c XE
-- PHI → tablespace PHI_DATA  |  NCLOB para texto largo
-- =============================================================

BEGIN
  EXECUTE IMMEDIATE '
    CREATE TABLE PAC_HISTORIAL_CLINICO (
      HISTORIAL_ID      NUMBER         GENERATED BY DEFAULT ON NULL AS IDENTITY
                                       CONSTRAINT PK_PAC_HISTORIAL PRIMARY KEY,
      HOSPITAL_ID       NUMBER         NOT NULL,
      PAC_ID            NUMBER         NOT NULL,

      -- Tipo de entrada
      TIPO_ENTRADA      VARCHAR2(50)   NOT NULL,

      -- Contenido clínico (PHI)
      TITULO            VARCHAR2(200)  NOT NULL,
      DESCRIPCION       NCLOB          DEFAULT EMPTY_CLOB(),
      DIAGNOSTICO_CIE10 VARCHAR2(10)   DEFAULT '''' '''' NOT NULL,

      -- Médico responsable
      MEDICO_ID         NUMBER         DEFAULT NULL,

      -- Temporalidad
      FECHA_EVENTO      DATE           NOT NULL,

      -- Privacidad (solo el médico autor puede verlo)
      ES_PRIVADO        NUMBER(1)      DEFAULT 0 NOT NULL,

      -- Estado
      ACTIVO            NUMBER(1)      DEFAULT 1 NOT NULL,

      -- Auditoría
      CREATED_BY        NUMBER         DEFAULT NULL,
      CREATED_AT        TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
      UPDATED_BY        NUMBER         DEFAULT NULL,
      UPDATED_AT        TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,

      -- Restricciones
      CONSTRAINT CHK_HIS_TIPO   CHECK (TIPO_ENTRADA IN (
                                  ''''CONSULTA'''',''''HOSPITALIZACION'''',
                                  ''''CIRUGIA'''',''''LABORATORIO'''',
                                  ''''IMAGEN'''',''''MEDICAMENTO'''',
                                  ''''VACUNA'''',''''OTRO'''')),
      CONSTRAINT CHK_HIS_PRIV   CHECK (ES_PRIVADO IN (0, 1)),
      CONSTRAINT CHK_HIS_ACTIVO CHECK (ACTIVO     IN (0, 1))
    ) TABLESPACE PHI_DATA
  ';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF;
END;
/

