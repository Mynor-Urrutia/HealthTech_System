-- Trigger UPDATED_AT
BEGIN
  EXECUTE IMMEDIATE '
    CREATE OR REPLACE TRIGGER TRG_PAC_HISTORIAL_UPD
    BEFORE UPDATE ON PAC_HISTORIAL_CLINICO
    FOR EACH ROW
    BEGIN
      :NEW.UPDATED_AT := SYSTIMESTAMP;
