-- =============================================================
-- HealthTech Solutions — DDL: PAC_ALERGIAS
-- Módulo M02 — Registro de Alergias del Paciente
-- Compatible: Oracle 19c RAC + Oracle 21c XE
-- PHI → tablespace PHI_DATA (cifrado con TDE en PROD)
-- VPD: columna HOSPITAL_ID obligatoria
-- =============================================================

BEGIN
  EXECUTE IMMEDIATE '
    CREATE TABLE PAC_ALERGIAS (
      ALERGIA_ID       NUMBER         GENERATED BY DEFAULT ON NULL AS IDENTITY
                                      CONSTRAINT PK_PAC_ALERGIAS PRIMARY KEY,
      HOSPITAL_ID      NUMBER         NOT NULL,
      PAC_ID           NUMBER         NOT NULL,

      -- Clasificación
      TIPO_ALERGIA     VARCHAR2(50)   NOT NULL,
      AGENTE           VARCHAR2(200)  NOT NULL,
      REACCION         VARCHAR2(500)  DEFAULT '''' ''''  NOT NULL,
      SEVERIDAD        VARCHAR2(20)   DEFAULT ''''MODERADA'''' NOT NULL,
      VERIFICADA       NUMBER(1)      DEFAULT 0  NOT NULL,
      FECHA_DETECCION  DATE           DEFAULT NULL,
      OBSERVACIONES    VARCHAR2(1000) DEFAULT '''' '''' NOT NULL,

      -- Estado
      ACTIVO           NUMBER(1)      DEFAULT 1  NOT NULL,

      -- Auditoría
      CREATED_BY       NUMBER         DEFAULT NULL,
      CREATED_AT       TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
      UPDATED_BY       NUMBER         DEFAULT NULL,
      UPDATED_AT       TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,

      -- Restricciones
      CONSTRAINT CHK_ALG_TIPO      CHECK (TIPO_ALERGIA IN (
                                     ''''MEDICAMENTO'''',''''ALIMENTO'''',
                                     ''''AMBIENTAL'''',''''LATEX'''',''''OTRO'''')),
      CONSTRAINT CHK_ALG_SEVERIDAD CHECK (SEVERIDAD IN (
                                     ''''LEVE'''',''''MODERADA'''',
                                     ''''SEVERA'''',''''ANAFILACTICA'''')),
      CONSTRAINT CHK_ALG_ACTIVO    CHECK (ACTIVO    IN (0, 1)),
      CONSTRAINT CHK_ALG_VER       CHECK (VERIFICADA IN (0, 1))
    ) TABLESPACE PHI_DATA
  ';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF;
END;
/

-- FK: Paciente (ON DELETE CASCADE — si se elimina lógicamente el paciente)
BEGIN
  EXECUTE IMMEDIATE '
    ALTER TABLE PAC_ALERGIAS
    ADD CONSTRAINT FK_ALG_PACIENTE
      FOREIGN KEY (PAC_ID) REFERENCES PAC_PACIENTES(PAC_ID)
  ';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE = -2275 THEN NULL; ELSE RAISE; END IF;
END;
/

-- FK: Usuario creador
BEGIN
  EXECUTE IMMEDIATE '
    ALTER TABLE PAC_ALERGIAS
    ADD CONSTRAINT FK_ALG_CREATED_BY
      FOREIGN KEY (CREATED_BY) REFERENCES SEC_USUARIOS(USR_ID)
  ';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE = -2275 THEN NULL; ELSE RAISE; END IF;
END;
/

-- Índice de búsqueda por paciente (el más frecuente)
BEGIN EXECUTE IMMEDIATE 'CREATE INDEX IDX_ALG_PACIENTE ON PAC_ALERGIAS(HOSPITAL_ID, PAC_ID)';
EXCEPTION WHEN OTHERS THEN IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF; END;
/

-- Índice por severidad (alertas críticas)
BEGIN EXECUTE IMMEDIATE 'CREATE INDEX IDX_ALG_SEVERIDAD ON PAC_ALERGIAS(HOSPITAL_ID, SEVERIDAD)';
EXCEPTION WHEN OTHERS THEN IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF; END;
/

-- Trigger UPDATED_AT
BEGIN
  EXECUTE IMMEDIATE '
    CREATE OR REPLACE TRIGGER TRG_PAC_ALERGIAS_UPD
    BEFORE UPDATE ON PAC_ALERGIAS
    FOR EACH ROW
    BEGIN
      :NEW.UPDATED_AT := SYSTIMESTAMP;
    END;
  ';
EXCEPTION WHEN OTHERS THEN IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF;
END;
/

COMMENT ON TABLE  PAC_ALERGIAS             IS 'Catálogo de alergias por paciente (PHI - HIPAA)';
COMMENT ON COLUMN PAC_ALERGIAS.HOSPITAL_ID IS 'Partición VPD';
COMMENT ON COLUMN PAC_ALERGIAS.TIPO_ALERGIA IS 'MEDICAMENTO|ALIMENTO|AMBIENTAL|LATEX|OTRO';
COMMENT ON COLUMN PAC_ALERGIAS.SEVERIDAD   IS 'LEVE|MODERADA|SEVERA|ANAFILACTICA';
