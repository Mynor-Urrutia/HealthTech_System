-- Hospitales
BEGIN
  EXECUTE IMMEDIATE
    'CREATE SEQUENCE SEQ_SEC_HOSPITALES
     START WITH 1 INCREMENT BY 1
     CACHE 50 NOORDER NOCYCLE';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE = -955 THEN NULL; ELSE RAISE; END IF;
END;
