-- HealthTech Solutions — Sequences Módulo Seguridad (SEC)
-- CACHE 50: evita contención en Oracle 19c RAC (ambos nodos)
-- NOORDER: aceptable para PKs — no requieren orden estricto
-- Compatible: Oracle 19c RAC y Oracle 21c XE
